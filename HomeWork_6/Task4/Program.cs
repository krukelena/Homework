{  /* Машинки
    Полную структуру классов и их взаимосвязь продумать самостоятельно.
    Исходные данные база (массив) из n машин задать непосредственно в коде
    Создать базовый класс Auto с методами позволяющим вывести на экран информацию о транспортном средстве, 
    а также определить грузоподъемность транспортного средства.
    Создать производные классы: 
    Легковая_машина (марка, номер, скорость, грузоподъемность),
    Мотоцикл (марка, номер, скорость, грузоподъемность, наличие коляски, при этом если коляска отсутствует, то грузоподъемность равна 0), 
    Грузовик (марка, номер, скорость, грузоподъемность, наличие прицепа, при этом если есть прицеп, 
    то грузоподъемность увеличивается в два раза) со своими методами вывода информации на экран, и определения грузоподъемности.
    Создать базу (массив) из n машин, вывести полную информацию из базы на экран, а также организовать поиск машин, 
