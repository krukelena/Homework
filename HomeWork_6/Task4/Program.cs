    public static void Main(string[] args)
    {
        Auto[] autos = new Auto[5];

        Truck truck = new Truck("Ford", "TE 3345-7", 90, 30, false);
        truck.IsThereTrailer = true;

        autos[0] = new Truck("Chevrolet", "AB 2709-4", 80, 20, true);
        autos[1] = new Truck("Ford", "TE 3345-7", 90, 30, false);
        autos[2] = new PassengerCar("Renault", "0319 AO-3", 100, 10);
        autos[3] = new Motorcycle("Honda", "AB-4 5516", 110, 15, true);
        autos[4] = new Motorcycle("Kawasaki", "AE-1 0921", 120, 10, false);

